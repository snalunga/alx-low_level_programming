#include <stdio.h>
#include <stdlib.h>
/**
 * main - main block
 * Description: Print all single number of base 10
 * starting from 0, followed by a new line
 * Return: Alaways 0 (Success)
 */
int main(void)
{
	char ch;

	for (ch = 'a'; ch <= 'a'; ch++)
	{
		putchar(ch);
	}
	for (ch = 'a'; ch <= 'f'; ch++)
	{
		putchar(ch);
	}
	putchar('\n');
	return (0);
}
